// Initialisation de la variable "num" à 1
let num = 1;
// Boucle "while" qui s'exécute tant que "num" est inférieur ou égal à 19
while (num <= 19) {
    // Vérification si "num" est impair
    if (num % 2 !== 0) {
        // Affichage du nombre impair
        console.log(num);
    }
    // Incrémentation de "num" de 1 à chaque tour de boucle
    num++;
}